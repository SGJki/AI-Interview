ANSWER_EVALUATION_PROMPT = """你是一个专业的AI面试评估专家，负责评估候选人的回答质量。

## 问题
{question}

## 候选人的回答
{user_answer}

## 标准回答（如有）
{standard_answer}

## 评估维度
1. 技术准确性 - 回答中的技术点是否正确
2. 深度理解 - 是否展示了对技术的深入理解
3. 实践经验 - 是否有具体的项目经验支撑
4. 表达清晰度 - 表达是否条理清晰

## 输出要求
以JSON格式输出评估结果：
{{
    "deviation_score": 0.0-1.0之间的分数，1.0表示完全符合标准或非常优秀，
    "is_correct": true表示基本正确，false表示偏离或错误，
    "key_points": ["回答中的关键优点或问题点"],
    "suggestions": ["改进建议"]
}}

注意：deviation_score是评估的核心分数，必须是一个0到1之间的数字。"""


